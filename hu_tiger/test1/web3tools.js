    async init_web3() {
        if (typeof window.ethereum !== 'undefined') {
            const net_version = await ethereum.request({
                method: 'net_version'
            });
            if (net_version == 4) {
                this.web3 = new Web3(ethereum);
                try {
                    const accounts = await ethereum.request({
                        method: 'eth_requestAccounts'
                    });
                    this.from_addr = accounts[0];
                    $(".connected-img").show();
                    $(".connect-img").hide();
                    return '1'
                } catch (error) {
                    return error
                }
            } else {
                $("#tip1").show();
                return '2'
            }
        } else {
            return '0'
        }
    };
    async verifyMateMask() {
        return await this.init_web3()
    };
    async checkout(token) {
        let rs = await this.verifyMateMask();
        if (rs == '1') {
            this.sendMint(token)
        } else if (rs == '0') {
            $("#tip2").show();
            return {
                code: 201,
                msg: 'no matemask'
            }
        } else {
            return {
                code: 400,
                error: rs
            }
        }
    };


    sendMintNew(count) {
        let _instance = new this.web3.eth.Contract(this._abi, this._contract_addr);
        var that = this;
        _instance.methods.totalMint().call().then((_total) => {
            let buy_count = Number(count);
            var s_count = 9900 - _total;
            console.log(s_count);
            if (_total <= 9900) {
                let __count = 0;
                if (buy_count > s_count) {
                    $(".step2").hide();
                    $(".sold-out-wrapper").show();
                } else{
                    buy_count < s_count ? __count = buy_count :  __count = s_count;
                    _instance.methods.price(__count).call().then((_price) => {
                        console.log(_price);
                        if (_price > 0) {
                            let _transaction = {
                                'from': that.from_addr,
                                'gas': 30 * 10000,
                                'value': _price
                            };
                            _instance.methods.mint(that.from_addr, __count).send(_transaction).on('transactionHash', function(hash) {
                                console.log(hash)
                            }).on('receipt', function(receipt) {
                                console.log(receipt);
                                $(".step2").hide();
                                $(".step3").show()
                            }).on('error', function(error) {
                                console.log(error);
                                $(".step2").hide();
                                $(".step3").hide();
                                $(".step1").hide();
                                $(".modal").fadeOut()
                            })
                        }
                    })
                }
                
            }else {
                $(".step2").hide();
                $(".sold-out-wrapper").show()
            }
        })
    };
